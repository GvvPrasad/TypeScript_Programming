/*
do-while loop iterates the elements for the infinite number of times similar to the while loop. But there is one difference from while loop, i.e., it gets executed at least once whether the condition is true or false.
use do-while loop when you want to execute the block of code at least once, and then repeat the loop as long as a specified condition is true.
*/

let n = 10;  
  
do {   
    console.log(n);   
    n++;   
 } while(n<=15);